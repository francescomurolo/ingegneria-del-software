package socketClasses;

/**
 * Object types that can be used.
 */
public enum ObjectType {
    /**
     * Wine object.
     */
    wine,

    /**
     * Request object.
     */
    request,

    /**
     * Order object.
     */
    order,

    /**
     * Employee object.
     */
    employee,

    /**
     * User object.
     */
    user,

    /**
     * Person object.
     */
    person,

    /**
     * Request object not seen by the user.
     */
    unSeenRequest,
}
